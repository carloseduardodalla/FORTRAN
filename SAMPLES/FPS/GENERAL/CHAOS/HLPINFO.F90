SUBROUTINE HELP_INFO_SRC3(CHECKED)
!*****************************************************************************
!*
!* THIS SUBROUTINE BRINGS UP A GENERAL HELP DIALOG BOX FOR THE PROGRAM
!*
!*****************************************************************************

  USE MSFLIB
  USE DIALOGM

  IMPLICIT NONE

  INCLUDE 'RESOURCE.FD'

  TYPE(DIALOG)DLG

  LOGICAL(KIND=4)RET
  INTEGER(KIND=4)IRET, I
  CHARACTER(LEN=120)CTEMP1
  LOGICAL(KIND=4)CHECKED

  CALL UNUSEDQQ(CHECKED)

!* INITIALIZE THE DIALOG BOX
  RET = DLGINIT(IDD_MORE_INFO_SRC3, DLG)
  RET = DLGSET(DLG, IDC_LIST1_MORE_INFO_SRC3,200,DLG_NUMITEMS)
  OPEN(UNIT=200,FILE='README.TXT',STATUS='OLD', ERR=1000)
  I = 1
  DO WHILE (.NOT. EOF(200))
    READ(200,'(A120)')CTEMP1
	I = I+1
    RET = DLGSET(DLG, IDC_LIST1_MORE_INFO_SRC3,CTEMP1,I)
  END DO

!* BRING UP THE DIALOG BOX
  IRET = DLGMODAL(DLG)

!* EXIT BUTTON
  IF(IRET == IDC_MORE_INFO_SRC3_EXIT)THEN
    !* DESTROY AND RELEASE THE DIALOG RESOURCES
    CALL DLGUNINIT(DLG)
  ENDIF

  GO TO 999
1000 CONTINUE
  IRET = MESSAGEBOXQQ('ERROR OPENING HELP TEXT FILE'C,'ERROR'C,MB$ICONSTOP .OR. MB$OK)

999  CONTINUE
  REWIND(UNIT=200)
  CLOSE(UNIT=200)
  RETURN
END SUBROUTINE HELP_INFO_SRC3
